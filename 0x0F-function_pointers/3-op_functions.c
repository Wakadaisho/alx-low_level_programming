#include "3-calc.h"

/**
* op_add - return the sum of two integers
*
* @a: first operand
* @b: second operand
*
* Return: integer
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
* op_sub - return the difference between two integers
*
* @a: first operand
* @b: second operand
*
* Return: integer
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
* op_mul - return the multiple of two integers
*
* @a: first operand
* @b: second operand
*
* Return: integer
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
* op_div - return the integer ratio of two integers
*
* @a: first operand
* @b: second operand
*
* Return: integer
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
* op_mod - return the mod value betwen two integers
*
* @a: first operand
* @b: second operand
*
* Return: integer
*/
int op_mod(int a, int b)
{
	return (a % b);
}
